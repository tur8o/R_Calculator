#Building a simple calculator in R Script

addition <- function(x, y) {
  return(x + y)
}

addition(1,2)

subtraction <- function(x, y) {
  return(x - y)
}

subtraction(1,2)

division <- function(x, y) {
  return(x / y)
}

division(1,2)

multiplication <- function(x, y) {
  return(x * y)
}

multiplication(1,2)
